      <h1>EMPOWERMENT</h1>
      <p>Our desire is for every child to be free to make the right life choices and take up the power to pursue these with personal responsibility and self-determination. As children discover self-worth, we support them to also discover their power to break through the limits and overcome the sense of defeat, powerlessness and lack of influence over their own situations. 

Education is at the core of our empowerment strategy. That is why the goal of our education initiatives goes beyond bare literacy but focuses on providing access to information and training that help the children to question their world and experiences as they become aware of how they can use their own abilities and the resources available to them to improve their own lives and change the world. 

We provide life skills training to children and empower them with valuable skills in technology and various vocational fields including music, fashion, arts and creativity. Ultimately, we believe empowering the child has a direct impact on lifting households and communities from poverty. This is the way we address the broader issues of inclusive economic growth, social order and security.
