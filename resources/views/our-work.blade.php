      <h1>Care and Rehabilitation</h1>
      <p>Our first intervention in the lives of children is ensuring their wellbeing. The displaced, abused, neglected or orphaned child often requires the basic amenities for living and a chance at survival for any other intervention to be sustainable. 

Through an in-centre care programme, we cater to children at risk and provide for their basic needs such as shelter, food, clothing and medical care. Our goal is to assist every child recover from the trauma of negative life experiences and rewrite a new story for themselves as they return to a healthy life in a safe and loving home environment. 
 
Currently, we have three residential learning centres in Lagos, Ibadan and Igboora. All our centres are government-approved in every state we operate. Our approach at every centre is the same. We work for the total development of every child including character, spirituality, creativity and personal responsibility. Our work would not have been done by merely meeting children’s needs for survival alone. Importantly, we want to see them discover their identity and a sense of meaning and role in recreating the future of their country. 
